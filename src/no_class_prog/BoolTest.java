class BoolTest{
    public static void main(String[] args){
        
        System.out.println(1 > 2);
        System.out.println(1 < 2);
        System.out.println(8 >= 9);
        System.out.println(11 <= 11);
        System.out.println(121 == 212);
        System.out.println(121 != 212);
        System.out.println(! (121 == 212));
        System.out.println( (42 >= 32) && (!(true)) );
        System.out.println( (42 >= 32) || (!(true)) );
    }
}
